/// GSTR-2B document generation status.
enum Gstr2bStatus { generated, notGenerated, processing }

/// Immutable result of fetching a GSTR-2B statement from GSTN.
///
/// All monetary amounts are stored in paise (integer) to avoid
/// floating-point precision issues.
class Gstr2bFetchResult {
  const Gstr2bFetchResult({
    required this.gstin,
    required this.period,
    required this.status,
    required this.totalIgstCredit,
    required this.totalCgstCredit,
    required this.totalSgstCredit,
    required this.entryCount,
    this.generatedAt,
  });

  /// 15-character GSTIN of the taxpayer.
  final String gstin;

  /// Filing period in MMYYYY format, e.g. "032024".
  final String period;

  /// Whether the GSTR-2B document has been generated by GSTN.
  final Gstr2bStatus status;

  /// Total eligible IGST credit (in paise).
  final int totalIgstCredit;

  /// Total eligible CGST credit (in paise).
  final int totalCgstCredit;

  /// Total eligible SGST credit (in paise).
  final int totalSgstCredit;

  /// Number of supplier entries in the GSTR-2B document.
  final int entryCount;

  /// Timestamp when GSTN generated this GSTR-2B document.
  final DateTime? generatedAt;

  /// Total ITC credit across all tax heads (in paise).
  int get totalCredit => totalIgstCredit + totalCgstCredit + totalSgstCredit;

  Gstr2bFetchResult copyWith({
    String? gstin,
    String? period,
    Gstr2bStatus? status,
    int? totalIgstCredit,
    int? totalCgstCredit,
    int? totalSgstCredit,
    int? entryCount,
    DateTime? generatedAt,
  }) {
    return Gstr2bFetchResult(
      gstin: gstin ?? this.gstin,
      period: period ?? this.period,
      status: status ?? this.status,
      totalIgstCredit: totalIgstCredit ?? this.totalIgstCredit,
      totalCgstCredit: totalCgstCredit ?? this.totalCgstCredit,
      totalSgstCredit: totalSgstCredit ?? this.totalSgstCredit,
      entryCount: entryCount ?? this.entryCount,
      generatedAt: generatedAt ?? this.generatedAt,
    );
  }

  @override
  bool operator ==(Object other) =>
      identical(this, other) ||
      other is Gstr2bFetchResult &&
          runtimeType == other.runtimeType &&
          gstin == other.gstin &&
          period == other.period &&
          status == other.status &&
          totalIgstCredit == other.totalIgstCredit &&
          totalCgstCredit == other.totalCgstCredit &&
          totalSgstCredit == other.totalSgstCredit &&
          entryCount == other.entryCount &&
          generatedAt == other.generatedAt;

  @override
  int get hashCode => Object.hash(
    gstin,
    period,
    status,
    totalIgstCredit,
    totalCgstCredit,
    totalSgstCredit,
    entryCount,
    generatedAt,
  );
}
